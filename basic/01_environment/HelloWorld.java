/**
 * Java入门第一个程序
 * 演示Java程序的基本结构和输出语句的使用
 */
public class HelloWorld {
    public static void main(String[] args) {
        // 输出Hello World到控制台
        System.out.println("Hello World!");
        
        // 演示不同的输出方式
        System.out.print("这是print输出"); // 不换行
        System.out.println("这是println输出"); // 换行
        System.out.printf("这是格式化输出：%d", 100076); // 格式化输出
    }
} 